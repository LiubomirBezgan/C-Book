/*  Includes ------------------------------------------------------------------*/
#include <stdio.h>

/*  Main ----------------------------------------------------------------------*/
int main(void)
{
/*  Variables -----------------------------------------------------------------*/
	int ch;
	
/*  Code ----------------------------------------------------------------------*/
	while ((ch = getchar()) != EOF)
		putchar(ch);
	return 0;
}